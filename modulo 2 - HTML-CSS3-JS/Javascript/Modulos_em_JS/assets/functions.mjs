function mostrarIdade (name, age) {
  return `A idade de ${name} é ${age}`
}

function mostrarCidade (city, state) {
  return `A cidade é ${city} localizada em ${state}`
}

function mostrarHobby (name, hobby) {
  return `O hobby preferido de ${name} é ${hobby}`
}

export {
  mostrarIdade,
  mostrarCidade,
  mostrarHobby
}